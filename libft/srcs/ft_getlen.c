#include "libft.h"

/*
** get length of number
** @param nbr
** @return
*/

int			get_len(int nbr)
{
	int	len;

	len = 0;
	if (nbr <= 0)
		++len;
	while (nbr != 0)
	{
		++len;
		nbr /= 10;
	}
	return (len);
}
